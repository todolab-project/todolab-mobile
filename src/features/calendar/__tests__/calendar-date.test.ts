import { getWeekDates } from '../calendar-date';

describe('getWeekDates', () => {
  it('월요일부터 일요일까지 한 주를 반환한다', () => {
    expect(getWeekDates('2026-06-28')).toEqual([
      '2026-06-22',
      '2026-06-23',
      '2026-06-24',
      '2026-06-25',
      '2026-06-26',
      '2026-06-27',
      '2026-06-28',
    ]);
  });

  it('월과 연도가 바뀌는 주도 계산한다', () => {
    expect(getWeekDates('2026-01-01')).toEqual([
      '2025-12-29',
      '2025-12-30',
      '2025-12-31',
      '2026-01-01',
      '2026-01-02',
      '2026-01-03',
      '2026-01-04',
    ]);
  });
});
